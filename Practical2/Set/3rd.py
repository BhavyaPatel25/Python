set1 = {14,26,17,36,22}
set2 = {10,14,28,39,26}

# Intersection of set is performed using the intersection function.
intersection_set = set1.intersection(set2)
print(intersection_set)

# Union of two set is performed using the union function.
union_set = set1.union(set2)
print(union_set)

# Difference of two set is performed using the difference function
difference_set = set1.difference(set2)
print(difference_set)